func (dfq *DbFilterQuery) loadUser(ctx context.Context, query *DbUserQuery, nodes []*DbFilter, init func(*DbFilter), assign func(*DbFilter, *DbUser)) error {
	edgeIDs := make([]driver.Value, len(nodes))
	byID := make(map[int]*DbFilter)
	nids := make(map[int]map[*DbFilter]struct{})
	for i, node := range nodes {
		edgeIDs[i] = node.ID
		byID[node.ID] = node
		if init != nil {
			init(node)
		}
	}
	query.Where(func(s *sql.Selector) {
		joinT := sql.Table(dbfilter.UserTable)
		s.Join(joinT).On(s.C(dbuser.FieldID), joinT.C(dbfilter.UserPrimaryKey[0]))
		s.Where(sql.InValues(joinT.C(dbfilter.UserPrimaryKey[1]), edgeIDs...))
		columns := s.SelectedColumns()
		s.Select(joinT.C(dbfilter.UserPrimaryKey[1]))
		s.AppendSelect(columns...)
		s.SetDistinct(false)
	})
	if err := query.prepareQuery(ctx); err != nil {
		return err
	}
	neighbors, err := query.sqlAll(ctx, func(_ context.Context, spec *sqlgraph.QuerySpec) {
		assign := spec.Assign
		values := spec.ScanValues
		spec.ScanValues = func(columns []string) ([]interface{}, error) {
			values, err := values(columns[1:])
			if err != nil {
				return nil, err
			}
			return append([]interface{}{new(sql.NullInt64)}, values...), nil
		}
		spec.Assign = func(columns []string, values []interface{}) error {
			outValue := int(values[0].(*sql.NullInt64).Int64)
			inValue := int(values[1].(*sql.NullInt64).Int64)
			if nids[inValue] == nil {
				nids[inValue] = map[*DbFilter]struct{}{byID[outValue]: struct{}{}}
				return assign(columns[1:], values[1:])
			}
			nids[inValue][byID[outValue]] = struct{}{}
			return nil
		}
	})
	if err != nil {
		return err
	}
	for _, n := range neighbors {
		nodes, ok := nids[n.ID]
		if !ok {
			return fmt.Errorf(`unexpected "user" node returned %v`, n.ID)
		}
		for kn := range nodes {
			assign(kn, n)
		}
	}
	return nil
}

func (dfq *DbFilterQuery) sqlCount(ctx context.Context) (int, error) {
	_spec := dfq.querySpec()
	_spec.Node.Columns = dfq.fields
	if len(dfq.fields) > 0 {
		_spec.Unique = dfq.unique != nil && *dfq.unique
	}
	return sqlgraph.CountNodes(ctx, dfq.driver, _spec)
}

func (dfq *DbFilterQuery) sqlExist(ctx context.Context) (bool, error) {
	n, err := dfq.sqlCount(ctx)
	if err != nil {
		return false, fmt.Errorf("ent: check existence: %w", err)
	}
	return n > 0, nil
}

func (dfq *DbFilterQuery) querySpec() *sqlgraph.QuerySpec {
	_spec := &sqlgraph.QuerySpec{
		Node: &sqlgraph.NodeSpec{
			Table:   dbfilter.Table,
			Columns: dbfilter.Columns,
			ID: &sqlgraph.FieldSpec{
				Type:   field.TypeInt,
				Column: dbfilter.FieldID,
			},
		},
		From:   dfq.sql,
		Unique: true,
	}
	if unique := dfq.unique; unique != nil {
		_spec.Unique = *unique
	}
	if fields := dfq.fields; len(fields) > 0 {
		_spec.Node.Columns = make([]string, 0, len(fields))
		_spec.Node.Columns = append(_spec.Node.Columns, dbfilter.FieldID)
		for i := range fields {
			if fields[i] != dbfilter.FieldID {
				_spec.Node.Columns = append(_spec.Node.Columns, fields[i])
			}
		}
	}
	if ps := dfq.predicates; len(ps) > 0 {
		_spec.Predicate = func(selector *sql.Selector) {
			for i := range ps {
				ps[i](selector)
			}
		}
	}
	if limit := dfq.limit; limit != nil {
		_spec.Limit = *limit
	}
	if offset := dfq.offset; offset != nil {
		_spec.Offset = *offset
	}
	if ps := dfq.order; len(ps) > 0 {
		_spec.Order = func(selector *sql.Selector) {
			for i := range ps {
				ps[i](selector)
			}
		}
	}
	return _spec
}

func (dfq *DbFilterQuery) sqlQuery(ctx context.Context) *sql.Selector {
	builder := sql.Dialect(dfq.driver.Dialect())
	t1 := builder.Table(dbfilter.Table)
	columns := dfq.fields
	if len(columns) == 0 {
		columns = dbfilter.Columns
	}
	selector := builder.Select(t1.Columns(columns...)...).From(t1)
	if dfq.sql != nil {
		selector = dfq.sql
		selector.Select(selector.Columns(columns...)...)
	}
	if dfq.unique != nil && *dfq.unique {
		selector.Distinct()
	}
	for _, p := range dfq.predicates {
		p(selector)
	}
	for _, p := range dfq.order {
		p(selector)
	}
	if offset := dfq.offset; offset != nil {
		// limit is mandatory for offset clause. We start
		// with default value, and override it below if needed.
		selector.Offset(*offset).Limit(math.MaxInt32)
	}
	if limit := dfq.limit; limit != nil {
		selector.Limit(*limit)
	}
	return selector
}

// DbFilterGroupBy is the group-by builder for DbFilter entities.
type DbFilterGroupBy struct {
	config
	selector
	fields []string
	fns    []AggregateFunc
	// intermediate query (i.e. traversal path).
	sql  *sql.Selector
	path func(context.Context) (*sql.Selector, error)
}

// Aggregate adds the given aggregation functions to the group-by query.
func (dfgb *DbFilterGroupBy) Aggregate(fns ...AggregateFunc) *DbFilterGroupBy {
	dfgb.fns = append(dfgb.fns, fns...)
	return dfgb
}

// Scan applies the group-by query and scans the result into the given value.
func (dfgb *DbFilterGroupBy) Scan(ctx context.Context, v interface{}) error {
	query, err := dfgb.path(ctx)
	if err != nil {
		return err
	}
	dfgb.sql = query
	return dfgb.sqlScan(ctx, v)
}

func (dfgb *DbFilterGroupBy) sqlScan(ctx context.Context, v interface{}) error {
	for _, f := range dfgb.fields {
		if !dbfilter.ValidColumn(f) {
			return &ValidationError{Name: f, err: fmt.Errorf("invalid field %q for group-by", f)}
		}
	}
	selector := dfgb.sqlQuery()
	if err := selector.Err(); err != nil {
		return err
	}
	rows := &sql.Rows{}
	query, args := selector.Query()
	if err := dfgb.driver.Query(ctx, query, args, rows); err != nil {
		return err
	}
	defer rows.Close()
	return sql.ScanSlice(rows, v)
}

func (dfgb *DbFilterGroupBy) sqlQuery() *sql.Selector {
	selector := dfgb.sql.Select()
	aggregation := make([]string, 0, len(dfgb.fns))
	for _, fn := range dfgb.fns {
		aggregation = append(aggregation, fn(selector))
	}
	// If no columns were selected in a custom aggregation function, the default
	// selection is the fields used for "group-by", and the aggregation functions.
	if len(selector.SelectedColumns()) == 0 {
		columns := make([]string, 0, len(dfgb.fields)+len(dfgb.fns))
		for _, f := range dfgb.fields {
			columns = append(columns, selector.C(f))
		}
		columns = append(columns, aggregation...)
		selector.Select(columns...)
	}
	return selector.GroupBy(selector.Columns(dfgb.fields...)...)
}

// DbFilterSelect is the builder for selecting fields of DbFilter entities.
type DbFilterSelect struct {
	*DbFilterQuery
	selector
	// intermediate query (i.e. traversal path).
	sql *sql.Selector
}

// Scan applies the selector query and scans the result into the given value.
func (dfs *DbFilterSelect) Scan(ctx context.Context, v interface{}) error {
	if err := dfs.prepareQuery(ctx); err != nil {
		return err
	}
	dfs.sql = dfs.DbFilterQuery.sqlQuery(ctx)
	return dfs.sqlScan(ctx, v)
}

func (dfs *DbFilterSelect) sqlScan(ctx context.Context, v interface{}) error {
	rows := &sql.Rows{}
	query, args := dfs.sql.Query()
	if err := dfs.driver.Query(ctx, query, args, rows); err != nil {
		return err
	}
	defer rows.Close()
	return sql.ScanSlice(rows, v)
}
