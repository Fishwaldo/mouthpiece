func (dtrgb *DbTransportRecipientsGroupBy) sqlScan(ctx context.Context, v interface{}) error {
	for _, f := range dtrgb.fields {
		if !dbtransportrecipients.ValidColumn(f) {
			return &ValidationError{Name: f, err: fmt.Errorf("invalid field %q for group-by", f)}
		}
	}
	selector := dtrgb.sqlQuery()
	if err := selector.Err(); err != nil {
		return err
	}
	rows := &sql.Rows{}
	query, args := selector.Query()
	if err := dtrgb.driver.Query(ctx, query, args, rows); err != nil {
		return err
	}
	defer rows.Close()
	return sql.ScanSlice(rows, v)
}

func (dtrgb *DbTransportRecipientsGroupBy) sqlQuery() *sql.Selector {
	selector := dtrgb.sql.Select()
	aggregation := make([]string, 0, len(dtrgb.fns))
	for _, fn := range dtrgb.fns {
		aggregation = append(aggregation, fn(selector))
	}
	// If no columns were selected in a custom aggregation function, the default
	// selection is the fields used for "group-by", and the aggregation functions.
	if len(selector.SelectedColumns()) == 0 {
		columns := make([]string, 0, len(dtrgb.fields)+len(dtrgb.fns))
		for _, f := range dtrgb.fields {
			columns = append(columns, selector.C(f))
		}
		columns = append(columns, aggregation...)
		selector.Select(columns...)
	}
	return selector.GroupBy(selector.Columns(dtrgb.fields...)...)
}

