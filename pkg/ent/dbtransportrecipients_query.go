func (dtrq *DbTransportRecipientsQuery) sqlQuery(ctx context.Context) *sql.Selector {
	builder := sql.Dialect(dtrq.driver.Dialect())
	t1 := builder.Table(dbtransportrecipients.Table)
	columns := dtrq.fields
	if len(columns) == 0 {
		columns = dbtransportrecipients.Columns
	}
	selector := builder.Select(t1.Columns(columns...)...).From(t1)
	if dtrq.sql != nil {
		selector = dtrq.sql
		selector.Select(selector.Columns(columns...)...)
	}
	if dtrq.unique != nil && *dtrq.unique {
		selector.Distinct()
	}
	for _, p := range dtrq.predicates {
		p(selector)
	}
	for _, p := range dtrq.order {
		p(selector)
	}
	if offset := dtrq.offset; offset != nil {
		// limit is mandatory for offset clause. We start
		// with default value, and override it below if needed.
		selector.Offset(*offset).Limit(math.MaxInt32)
	}
	if limit := dtrq.limit; limit != nil {
		selector.Limit(*limit)
	}
	return selector
}

